      <h1 class="text-center"><u>Maison des Ligues</u></h1><br>
      <p>La Maison des ligues est un établissement du <b>Conseil Régional</b>. Ce n'est pas une entité juridique propre.</p>
      <p>Elle est financé à 100% (pour son fonctionnement et pour la construction récente de l'extension de certains bâtiments) 
        par le Conseil Régional et ans aucune participation du Conseil Général de Meurthe et Moselle, bien qu'elle abrite un certain nombre de
        comités départementaux.</p>
      <p>Une convention de cogestion a été passée entre le Conseil Régional et le Comité Régional Olympique et Sportif
        de Lorraine pour la gestion de l'outil "Maison des Ligues".</p>
      <p>Le <b>CROSL</b> est une association financée par le ministère via le <b>CNDS</b>  (Centre National de Développement du Sport).</p>
      <p>Lors de la repise de la Maison des Ligues en 2000 par la Région Lorraine et la négocaition qui en a suivi, le mouvement sportif a défendu la présence
        des comités départementaux dans les locaux existants. Cette présence est remise en question régulièrement, mais il est probable que rien ne sera fait
        avant la réforme des territoires et la "fusion" des conseillers régionaux et généraux en conseilleurs territoriaux.</p>
      <p>La Maison héberge la majorité des ligues sportives régionales, de la ligue de tennis (la plus grosse, qui emploie 8 personnes) à des ligues de sports qui 
        n'ont pas d'employés permanents, comme le bowling ou la plongée sous-marine.</p>
